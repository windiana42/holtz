<h2>Introduction</h2>

Welcome to Holtz!<br>
With Holtz, you can play the abstract strategy games <a href="zertz.php">Z&egrave;rtz</a> and <a href="dvonn.php">Dvonn</a>. Both of them are board games&nbsp;invented by Kris Burm, and are part of the <a href="http://www.gipf.com">GIPF project</a>.


<h2>Z&egrave;rtz:: The Game</h2>

Z&egrave;rtz is a board game similar sophisticated than chess. 
It is played by two players.
The players alternately set or knock out stones <a href="zertz.php">according 
to several rules</a> and have to 
get a certain amount of white, gray or black stones to win.


<h2>Dvonn:: The Game</h2>

In Dvonn, players must form stacks <a href="dvonn.php">according to several rules</a>
to capture the opponent's pieces, but never lose connection to the
special DVONN stones. The higher the stack, the faster it moves, so the
tension increases until the very end...


<h2>Holtz:: The Software</h2>

The <a href="helpprog.php">program Holtz</a> supports 
playing Z&egrave;rtz and Dvonn offline against human or computer players, 
or on any network (e.g. the Internet).<br>
There are various hints and help texts for beginners.<br>

Holtz is available for Windows and Linux for free. Download
and try it now!<br>
Holtz was developed using the <a href="http://www.wxwidgets.org">wxWidgets</a> GUI library.

